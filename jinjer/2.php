        $(document).ready(function(){
            // check icon checkbox
            $('.check-1').hide();
            $('.icon-nocheck').on({
                'click':function(){                    
                      $(".icon-nocheck").find("input").removeAttr('checked');
                      $(".icon-nocheck").removeClass('active-check');
                      $(this).find("input").attr('checked', true);
                      $(this).addClass("active-check");
                }
            });

           
            $(".error").hide(); 
            $("form").submit(function(){
             
                    var Ra = true;
                    var check1  = $('#check-1').is(':checked'); 
                    var check2  = $("#check-2").is(':checked');
                    var name              = $('#name').val();
                    var email             = $('#email').val();  
                    var emailConfirm      = $('#email-confirm').val();   
                    var emailReg          = /^([\w-\.]+@([\w-]+\.)+[\w-]{2,4})?$/;
                    var content             = $('#content').val(); 

                    if(check1 == true || check2 == true){ 
                         $(".err-inquiry-type").hide();
                        
                    } 
                    else{ 
                          $(".err-inquiry-type").show();
                          Ra = false;
                    }
                    // 
                    if(name == ""){ 
                         $(".err-name").show(); 
                         Ra = false;
                    } 
                    else{ $(".err-name").hide(); 
                   }
                    //
                    if(email != ""){ 
                          $(".err-email").hide();
                          if(emailReg.test(email)){
                                 $(".err-email-2").hide();
                                 if(email != emailConfirm){
                                        $(".err-email-3").show(); 
                                        Ra = false;
                                  }
                                  else{
                                         $(".err-email-3").hide(); 
                                  }
                          }
                          else{
                                 $(".err-email-2").show(); 
                                 Ra = false;
                          }
                    } 
                    else{ 
                          $(".err-email").show(); 
                          Ra = false;
                    }
                    //
                    if(emailConfirm != ""){ 
                          $(".err-email-confirm").hide();
                          if(emailReg.test(emailConfirm)){
                                 $(".err-email-confirm-2").hide();
                          }
                          else{
                                 $(".err-email-confirm-2").show(); 
                                 Ra = false;
                          }
                    } 
                    else{ 
                          $(".err-email-confirm").show(); 
                          Ra = false;
                    }
                    
                    
                    //
                    if(content == ""){ 
                         $(".err-content").show();
                         Ra = false;
                    } 
                    else{ $(".err-content").hide();}
    
    
                    if(Ra==true){
                        return true;
                    }   
                    else{ 
                        
                    return false;
                    
                    }   
            });
            
            
        });

        
